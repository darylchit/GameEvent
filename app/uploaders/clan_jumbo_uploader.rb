class ClanJumboUploader < CarrierWave::Uploader::Base

  include CarrierWave::RMagick
  # Include RMagick or MiniMagick support:
  # include CarrierWave::RMagick
  # include CarrierWave::MiniMagick

  # Choose what kind of storage to use for this uploader:
  #storage :file
  # storage :fog

  # Override the directory where uploaded files will be stored.
  # This is a sensible default for uploaders that are meant to be mounted:
  def store_dir
    "uploads/clans/#{model.class.to_s.underscore}/#{mounted_as}/#{model.id}"
  end

  # Process files as they are uploaded:
  # process :scale => [200, 300]
  #
  # def scale(width, height)
  #   # do something
  # end
  version :jumbo do
    process :resize_jumbo_image
  end

  def resize_jumbo_image
    img = Magick::Image.read(current_path)
    width = img[0].columns
    height = img[0].rows
    if width < 1900 || height < 670
      resize_to_fill(width, height)
    else
      resize_to_fill(1900, 670)
    end
  end

  # Create different versions of your uploaded files:
  # version :thumb do
